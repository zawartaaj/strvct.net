"use strict";

/*
    TouchMoveListener

    Listens to touch move events.
    Seperated from TouchListeners for performance reasons.

*/
 
(class TouchMoveListener extends EventSetListener {
    
    initPrototypeSlots () {
    }

    init () {
        super.init()
        return this
    } 

    setupListeners () {
        this.addEventNameAndMethodName("touchmove",   "onTouchMove");
        return this
    }

}.initThisClass());
